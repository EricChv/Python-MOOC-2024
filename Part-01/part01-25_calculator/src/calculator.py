num1 = int(input("Number 1: "))
num2 = int(input("Number 2: "))
operation = input("Operation: ")

if operation == "add":
    result = num1 + num2
    print(f"{num1} + {num2} = {result}" )


if operation == "subtract":
    result = num1 - num2
    print(f"{num1} - {num2} = {result}" )


if operation == "multiply":
    result = num1 * num2
    print(f"{num1} * {num2} = {result}" )




 